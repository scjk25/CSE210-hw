
public class Referance
{
   private string _book;
   private int _chapter;
   private int _verse;

   public string GetBook()
   {
        return _book;
   }

   public int GetChapter()
   {
        return _chapter;
   }

   public int GetVerse()
   {
        return _verse;
   }

   public void SetBook(string book)
   {
        _book = book;
   }

   public void SetChapter(int chapter)
   {
        _chapter = chapter;
   }

   public void SetVerse(int verse)
   {
        _verse = verse;
   }

}




